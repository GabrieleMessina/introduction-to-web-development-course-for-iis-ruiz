# 1. Creare un nuovo progetto sulla dashboard Firebase

# 2. Scaricare la CLI Firebase per poter sfruttare i loro comandi da Shell

# 3. Collega la cartella corrente al progetto Firebase create dalla loro dashboard
firebase init

# 4. Testiamo il nostro sito in locale prima della pubblicazione
firebase emulators:start 

# 5. Caricare i file del progetto su Firebase
firebase deploy

# 6. Et voilà andiamo a vedere cosa è successo sulla dashboard